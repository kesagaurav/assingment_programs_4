package Loops;

public class LoopsDemo1 {

	public static void main(String[] args) {
		for(int i=0;i<100;i+=2) {
			System.out.println("\n print the alternate elements are:" + i);
		}
		
		//while loop
		
		
		
		//do while loop
		int x=10;
		do {
			System.out.println(x);
			x++;
			
		}while(x<20);


	}

}
